class Command(BaseCommand):
    help = "Создаёт проект «Софт» и 8 этапов разработки ПО с подпунктами"

    def handle(self, *args, **options):
        proj, created = Project.objects.get_or_create(
            name_short="Софт",
            defaults={
                "name_full": "Разработка программного обеспечения",
                "code": "SOFT",
                "is_hidden": True,
            },
        )
        if not created and not proj.is_hidden:
            proj.is_hidden = True
            proj.save(update_fields=["is_hidden"])
        action = "создан" if created else "уже существует"
        self.stdout.write(f"Проект «Софт» (id={proj.id}) — {action}")

        order = 0
        created_count = 0
        for num, name, subs in STAGES:
            order += 1
            _, c = PPStage.objects.get_or_create(
                project=proj,
                stage_number=num,
                defaults={"name": name, "order": order},
            )
            if c:
                created_count += 1
            for sub_num, sub_name in subs:
                order += 1
                _, c = PPStage.objects.get_or_create(
                    project=proj,
                    stage_number=sub_num,
                    defaults={"name": sub_name, "order": order},
                )
                if c:
                    created_count += 1

        total = PPStage.objects.filter(project=proj).count()
        self.stdout.write(
            self.style.SUCCESS(
                f"Готово: создано {created_count} новых этапов, всего {total}"
            )
        )
